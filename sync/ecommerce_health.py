"""Детектор дневного провала сбора ecommerce.

Провал сбора выглядит на дашборде неотличимо от плохих продаж, поэтому синк обязан
кричать: 2026-07-06 KZ-срез дал 19 заказов на 8 882 визита (норма 110-270), 2026-07-08 —
39 на 9 598, и оба прогона отчитались успехом.

Сравниваем конверсию дня с МЕДИАНОЙ предыдущих дней, а не со средним: один уже
случившийся провал утащил бы среднее вниз, порог опустился бы вместе с ним, и следующий
такой же провал прошёл бы молча.

Отличить «сбор сломался» от «в этот день правда не покупали» по двум числам нельзя, и
детектор этого не утверждает — он просит проверить. Прогон по факту июня-июля 2026 дал три
срабатывания на 48 дней: 06.07 и 08.07 (настоящие провалы сбора) и 17.06 — канун
распродажи, когда трафик рос, а покупки откладывали до старта скидок 18.06. Порог под этот
день не подгоняли: подгонка под ретроспективу ослабила бы детектор на будущее, а одно
пограничное срабатывание в полтора месяца для WARN в логе приемлемо.
"""
from statistics import median


def detect_ecommerce_drop(
    visits: int,
    orders: int,
    baseline: list[tuple[int, int]],
    min_visits: int = 2000,
    ratio: float = 0.5,
) -> str | None:
    """Предупреждение, если конверсия дня обвалилась против базовой.

    Args:
        visits: визиты за день.
        orders: заказы за день.
        baseline: пары (визиты, заказы) предыдущих дней; дни без визитов игнорируются.
        min_visits: ниже этого трафика конверсия слишком шумная — молчим.
        ratio: доля от базовой конверсии, ниже которой это уже не колебание.

    Returns:
        Текст предупреждения либо None, если день выглядит нормально.
    """
    if visits < min_visits or not baseline:
        return None

    rates = [o / v for v, o in baseline if v > 0]
    if not rates:
        return None

    base = median(rates)
    if base <= 0:
        return None

    today = orders / visits
    if today >= base * ratio:
        return None

    expected = int(round(base * visits))
    return (
        f"конверсия в заказ {today * 100:.2f}% против базовой {base * 100:.2f}% "
        f"— заказов {orders} при ожидаемых ~{expected} на {visits} визитах. "
        f"Проверь сбор ecommerce за этот день"
    )
